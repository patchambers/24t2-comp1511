#include <stdio.h>

int main(void) {

    // Face 1:
    //  ~ ~
    //  0 0
    //   o
    //   -
    printf("Face 1: \n");
    printf("~ ~\n");
    printf("o o\n");
    printf(" o \n");
    printf(" - \n");
    
    // Face 2:
    //  ~ ~
    //  0 0
    //   o
    //  \_/
    printf("Face 2: \n");
    printf(
        "~ ~\n"
        "0 0\n"
        " o \n"
        "\\_/ \n"
    );

    return 0;
}





